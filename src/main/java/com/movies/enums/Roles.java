package com.movies.enums;

/**
 * Enum representing different roles in the application.
 */
public enum Roles {

    /**
     * Role assigned to regular users.
     */
    ROLE_USER,

    /**
     * Role assigned to moderators with additional privileges.
     */
    ROLE_MODERATOR,

    /**
     * Role assigned to administrators with full access.
     */
    ROLE_ADMIN
}
